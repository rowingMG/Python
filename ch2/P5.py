#Show results of basic operations

#User input
first = int(input('Enter first number: '))
second = int(input('Enter second number: '))

#Show results
print('{} + {} = {}'.format(first, second, first + second))
print('{} - {} = {}'.format(first, second, first - second))
print('{} * {} = {}'.format(first, second, first * second))
print('{} / {} = {}'.format(first, second, first / second))
print('{} // {} = {}'.format(first, second, first // second))
print('{} ** {} = {}'.format(first, second, first ** second))